    public static String removeFromWaitlist(String faculty, Date date, int seats){
         ArrayList<String> rooms = new ArrayList<>();
        ArrayList<String> roomsReserved = new ArrayList<>();
        roomsReserved = ReservationQueries.getJustRoomsReservedByDate(date);
        rooms = RoomQueries.getAllPossibleRooms(seats);
        
        
       String out = "";
        String room1;
        room1 = "";
        int i;
        for (i=0;i< rooms.size();i++ ) {
            room1 = rooms.get(i);
            if (rooms.size() == 0){
                break;
            }
            if (roomsReserved.contains(room1)){
                continue;
            }
            else{
                ReservationQueries.addReservationEntry(faculty, date, seats, room1);
                WaitlistQueries.cancelWaitlistEntry(faculty,date);
                out += "<html>" + faculty + " reserves " + room1 + " for " + date + " from waitlist " + "<br/>" +"<html>";
                
                break;
            }

        }
        return out;
    }
    
