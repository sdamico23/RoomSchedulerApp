    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        // TODO add your handling code here:
        String faculty;
        Date date;
        int seats;
        faculty =reserveFacultyComboBox.getSelectedItem().toString();
        date = Date.valueOf(reserveDateComboBox.getSelectedItem().toString());
        seats = Integer.parseInt(reserveNumberOfSeatsTextField.getText());
        ArrayList<String> rooms = new ArrayList<>();
        ArrayList<String> roomsReserved = new ArrayList<>();
        roomsReserved = ReservationQueries.getJustRoomsReservedByDate(date);
        rooms = RoomQueries.getAllPossibleRooms(seats);
        boolean waitlist;
        waitlist = false;
        String out,room;
        room = "";
        int i;
        for (i=0;i< rooms.size();i++ ) {
            room = rooms.get(i);
  
            if (roomsReserved.contains(room)){
                waitlist = true;
            }
            else{
                waitlist = false;
                break;
            }

        }
        if (rooms.size() == 0) {
                waitlist = true;
               
        }

        if (waitlist==false){
            ReservationQueries.addReservationEntry(faculty,date,seats, room);
            out = faculty + " reserves room " + room;
            reserveDisplayLabel.setText(out);
        }
        else{
            WaitlistQueries.addWaitlistEntry(faculty,date,seats);
            out = faculty + " gets waitlisted";
            reserveDisplayLabel.setText(out);

        }
    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

